		<p>A limestone bridge reflected its brick lined arches in the waters of the Chicques creek. It was lazy on its way through the 	pastures near Mt.Joy, PA. Two tall pine trees balanced the composition; their shadows were softened by a turpentine dampened rag. Every oil painter knows. Linneas Longenecker's Saturday morning plein air oil painting class. Nineteen-hundred-fifty-something-or-other; just a kid working beside older women looking for something to do for the weekend...<br /><br />

 A year or two later:<br /><br />

Sea gulls circled the masts. One looked for the perfect place to land. Swish. With three small deft brush strokes and it was permanently perched on the hand knotted ochre fishing nets of a Gloucester harbor dragger. I was mesmerized watching Emile Gruppe put the finishing touches on a New England coastal oil painting masterpiece while on a summer vacation camping trip. Motif Number One was waiting in the foggy distance for me the next morning. I returned the next year to be the 'errand boy' for the owner of a Beverly, MA tourist house hoping to get down to the harbor for more lessons.<br /><br />

  The following year:<br /><br />

A small tattered piece of linen kitchen tablecloth, with small holes,  was meticulously darned by the artist's grandmother. He (it may have been Joseph Cornell) stitched it to a canvas and signed his name. Her portrait. Exactly. To be contemplated; for sure. What was James McNeill Whistler thinking?<br />
 I looked back to see if others were awestruck by the eloquent simplicity. I looked over. I looked down. There was too much to see. Is this what Frank Lloyd Wright had in mind for the Guggenheims when we visited a couple of months after it opened? 
</p>
